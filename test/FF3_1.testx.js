const assert = require('assert');
const FF3_1 = require('../lib/FF3_1');

function test(key, twk, PT, CT, radix) {
    let out = String();
    ctx = FF3_1;

    assert.equal(PT.length, CT.length);

    ctx = new FF3_1(key, twk, radix);

    out = ctx.encrypt(PT);
    assert.equal(CT, out);

    out = ctx.decrypt(CT);
    assert.equal(PT, out);
}

